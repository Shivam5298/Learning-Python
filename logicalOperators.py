#This is a sample code to demonstrate the use of logical operators in Python
#Logical operators are used to combine conditional statements
#The logical operators in Python are: and, or, not

#Example of using 'and' operator
x = 5
y = 10
if x > 0 and y > 0:
    print("Both x and y are positive numbers.")

#Example of using 'or' operator
a = 3
b = -1

if a > 0 or b > 0:
    print("At least one of a or b is a positive number.")

#Example of using 'not' operator
c = False
if not c:
    print("c is False, so the condition is True.")

