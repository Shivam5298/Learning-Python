# This is a sample program to demonstrate comparison operators in Python.

num1 = 10
num2 = 20

print(num1 == num2)  # Equal to Output: False
print(num1 != num2)  # Not equal to Output: True
print(num1 < num2)   # Less than Output: True
print(num1 > num2)   # Greater than Output: False
print(num1 <= num2)  # Less than or equal to Output: True
print(num1 >= num2)  # Greater than or equal to Output: False
